    public void showFirstPanel() {
        Component[] components = VentanasInternalFrame.getContentPane().getComponents();
        List<Component> aux = new ArrayList<>();
        
        for(Component component:components){
            if(component instanceof JPanel){
                aux.add(component);
            }
        }
        
        aux.get(0).setVisible(true);
        aux.get(1).setVisible(false);
        aux.get(2).setVisible(false);
        aux.get(3).setVisible(false);
        aux.get(4).setVisible(false);
        
    }
    
    public void showPanel(Container container, String name){
        Component[] components = container.getComponents();
        
        for(Component component:components){
            if(component instanceof JPanel panel){
                if(panel.getName() != null && panel.getName().equals(name)){
                    panel.setVisible(true);
                } else {
                    panel.setVisible(false);
                }
            }
        }
    }
    
